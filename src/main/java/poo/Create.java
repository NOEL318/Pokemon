    /**
     * @param args the command line arguments
     */
    public void readFile() {
        try {
            search_results.setText("");
            String nombre = search_layout.getText();
            FileReader file_reader = new FileReader(file);
            BufferedReader buffered_reader = new BufferedReader(file_reader);
            String linea = "";
            String Text = search_results.getText();
            while ((linea = buffered_reader.readLine()) != null) {
                String[] pokemon = linea.split(",");
                if (pokemon[1].contentEquals(nombre)) {
                    Text = Text + pokemon[1] + "\n";
                    pokemon_Selected=pokemon;
                }
            }
             if(Text.equals("")){
                 enabled=false;
                    select_this_pokemon.setEnabled(enabled);
                    search_status.setText("Pokemon No Encontrado:");
             }else{
                 search_status.setText("Pokemon Encontrado:");
                 enabled=true;
                 select_this_pokemon.setEnabled(enabled);
             }
            search_results.setText(Text);
        } catch (FileNotFoundException ex) {

        } catch (IOException ex) {

        }
    }

